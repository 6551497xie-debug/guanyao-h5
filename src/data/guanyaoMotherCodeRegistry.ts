export const getMotherCodeDefinitionById = (
  motherCodeId: MotherCodeId,
): MotherCodeDefinition | undefined =>
  guanyaoMotherCodeRegistry.find((definition) => definition.motherCodeId === motherCodeId);

export const getMotherCodeDefinitionByTrigram = (
  trigram: Trigram,
): MotherCodeDefinition | undefined =>
  guanyaoMotherCodeRegistry.find((definition) => definition.trigram === trigram);

export const resolveMotherCodeDefinitionByIndex = (index: number): MotherCodeDefinition => {
  const normalizedIndex =
    ((index % guanyaoMotherCodeRegistry.length) + guanyaoMotherCodeRegistry.length) %
    guanyaoMotherCodeRegistry.length;

  return guanyaoMotherCodeRegistry[normalizedIndex];
};

